package cn.explink.domain;

/**
 * 自动分拣线的上货口类型
 * 
 * @author mali 2016年3月24日
 *  
 */
public class Entrance {
	private String entranceno;
	
	private String entranceip;
	
	private int enable;
	
	/**
	 * @return the entranceno
	 */
	public String getEntranceno() {
		return entranceno;
	}
	/**
	 * @param entranceno the entranceno to set
	 */
	public void setEntranceno(String entranceno) {
		this.entranceno = entranceno;
	}
	/**
	 * @return the entranceip
	 */
	public String getEntranceip() {
		return entranceip;
	}
	/**
	 * @param entranceip the entranceip to set
	 */
	public void setEntranceip(String entranceip) {
		this.entranceip = entranceip;
	}
	/**
	 * @return the enable
	 */
	public int getEnable() {
		return enable;
	}
	/**
	 * @param enable the enable to set
	 */
	public void setEnable(int enable) {
		this.enable = enable;
	}
	

}
