package cn.explink.domain;

public class AutomaticSet {
	private String nowlinkname;
	private String nextlink;
	private long isauto;

	public String getNowlinkname() {
		return nowlinkname;
	}

	public void setNowlinkname(String nowlinkname) {
		this.nowlinkname = nowlinkname;
	}

	public String getNextlink() {
		return nextlink;
	}

	public void setNextlink(String nextlink) {
		this.nextlink = nextlink;
	}

	public long getIsauto() {
		return isauto;
	}

	public void setIsauto(long isauto) {
		this.isauto = isauto;
	}

}
