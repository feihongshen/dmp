package cn.explink.b2c.saohuobang;

public class CargoInfo {

	String itemsname;// 商品名称
	String itemsId;// 商品编号
	String itemsBrandname;// 商品品牌名称
	String num;// 商品个数

	public String getItemsname() {
		return itemsname;
	}

	public void setItemsname(String itemsname) {
		this.itemsname = itemsname;
	}

	public String getItemsId() {
		return itemsId;
	}

	public void setItemsId(String itemsId) {
		this.itemsId = itemsId;
	}

	public String getItemsBrandname() {
		return itemsBrandname;
	}

	public void setItemsBrandname(String itemsBrandname) {
		this.itemsBrandname = itemsBrandname;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

}
