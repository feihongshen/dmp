public class Good {

	private String outgo_no;
	private String good_nm;
	private String good_qty;
	private double good_prc;

	public String getOutgo_no() {
		return outgo_no;
	}

	public void setOutgo_no(String outgo_no) {
		this.outgo_no = outgo_no;
	}

	public String getGood_nm() {
		return good_nm;
	}

	public void setGood_nm(String good_nm) {
		this.good_nm = good_nm;
	}

	public String getGood_qty() {
		return good_qty;
	}

	public void setGood_qty(String good_qty) {
		this.good_qty = good_qty;
	}

	public double getGood_prc() {
		return good_prc;
	}

	public void setGood_prc(double good_prc) {
		this.good_prc = good_prc;
	}

}
