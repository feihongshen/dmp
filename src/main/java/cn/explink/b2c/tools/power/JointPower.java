package cn.explink.b2c.tools.power;

public class JointPower {
	private int id;
	private long joint_num;
	private String remark;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public long getJoint_num() {
		return joint_num;
	}

	public void setJoint_num(long joint_num) {
		this.joint_num = joint_num;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
