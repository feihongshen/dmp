package cn.explink.b2c.suning.requestdto;

public class Order {
	private String deliveryno;
	private String work_id;
	private String col_pay_money;
	private String commdty_name;
	private String commdty_num;
	private String mark;
	public String getDeliveryno() {
		return deliveryno;
	}
	public void setDeliveryno(String deliveryno) {
		this.deliveryno = deliveryno;
	}
	public String getWork_id() {
		return work_id;
	}
	public void setWork_id(String work_id) {
		this.work_id = work_id;
	}
	public String getCol_pay_money() {
		return col_pay_money;
	}
	public void setCol_pay_money(String col_pay_money) {
		this.col_pay_money = col_pay_money;
	}
	public String getCommdty_name() {
		return commdty_name;
	}
	public void setCommdty_name(String commdty_name) {
		this.commdty_name = commdty_name;
	}
	public String getCommdty_num() {
		return commdty_num;
	}
	public void setCommdty_num(String commdty_num) {
		this.commdty_num = commdty_num;
	}
	public String getMark() {
		return mark;
	}
	public void setMark(String mark) {
		this.mark = mark;
	}
	
}
